#ifndef CEDAR_GUICONSTANTS_H
#define CEDAR_GUICONSTANTS_H


#define CEDAR_RENDERING_SHARP 0x00
#define CEDAR_RENDERING_SMOOTH 0x01


/**
 * Defines the horizontal alignment to be left.
 */
#define CEDAR_ALIGNMENT_LEFT 0x00u
/**
 * Defines the horizontal alignment to be center.
 */
#define CEDAR_ALIGNMENT_CENTER 0x01u
/**
 * Defines the horizontal alignment to be right.
 */
#define CEDAR_ALIGNMENT_RIGHT 0x02u
/**
 * Defines the vertical alignment to be top.
 */
#define CEDAR_ALIGNMENT_TOP 0x00u
/**
 * Defines the vertical alignment to be middle
 */
#define CEDAR_ALIGNMENT_MIDDLE 0x04u
/**
 * Defines the vertical alignment to be bottom.
 */
#define CEDAR_ALIGNMENT_BOTTOM 0x08u

#endif //CEDAR_GUICONSTANTS_H
